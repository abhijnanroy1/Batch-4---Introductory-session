class Abhi {
    public static void main(String args[])
    {
        String a1 = "Abhi01";
         String a2 = "Abhi02";
        int i = 4;
        int j = 6;
        
        if (i == 4) {
            i++;
            System.out.println(a1);
        }
         if (j == 6) {
            j++;
             System.out.println(a2);
        }
        
        System.out.println("i = " + i);
         System.out.println("j = " + j);
    }
}
